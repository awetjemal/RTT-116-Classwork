package org.perscholas;

public class AssignmentOperatorsDemo {
    public static void main(String[] args) {
        //assigning primitive values
        int j, k;
        j = 5;
        j = 10;
        k = 5;
        System.out.println(j + " + " + k + " = " + (j + k));
        //multiple assignment
        j = k = 12;
        System.out.println("j , k " + j + " , " + k);
    }
}
